public class switchcase {

        public switchcase(){
        }
        public static void main(String[] args) {
            int wage = 3200;
            int parttime = 160;
            switch(wage){
                case 1:
                    System.out.println("full time employee"+(wage));
                    break;
                case 2:
                    System.out.println("part time employee"+(parttime));
                    break;
            }
        }
    }
